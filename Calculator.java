    public int factorial(int x)
    {
        if(x==1)
	{
            return x;
	}
	else
	{
        	return (factorial(x-1)*x);
	}
    }
    public double permutation(double x,double y)
    {
        int n=(int)x,r=(int)y;
        r=n-r;
	n=factorial(n);
        r=factorial(r);
        r=n/r;
        double dd=(double)r;
	return dd;
    }
    public double combination(double x,double y)
    {
        int n=(int)x,r=(int)y,t;
        t=n-r;
        n=factorial(n);
        r=factorial(r);
        t=factorial(t);
        n=n/(t*r);
        double dd=(double)n;
        return dd;
    }
