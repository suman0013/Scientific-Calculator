    private void jdisplayKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jdisplayKeyTyped
        // TODO add your handling code here:
        char c=evt.getKeyChar();
        if(!(Character.isDigit(c) || (c==KeyEvent.VK_BACKSPACE) || (c==KeyEvent.VK_DELETE) || (c==KeyEvent.VK_C)))
        {
            getToolkit().beep();
            evt.consume();
        }
        if(c=='c' || c=='C')
        {
            jdisplay.setText("");
        }
    }//GEN-LAST:event_jdisplayKeyTyped

    private void jequalsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jequalsActionPerformed
        // TODO add your handling code here:
        String res;
        snum=Double.parseDouble(jdisplay.getText());
        if(operation=="+")
        {
            result=fnum+snum;
            res=String.format("%.2f",result);
            jdisplay.setText(res);
        }
        else if(operation=="-")
        {
            result=fnum-snum;
            res=String.format("%.2f",result);
            jdisplay.setText(res);
        }
        else if(operation=="*")
        {
            result=fnum*snum;
            res=String.format("%.2f",result);
            jdisplay.setText(res);
        }
        else if(operation=="/")
        {
            result=fnum/snum;
            res=String.format("%.2f",result);
            jdisplay.setText(res);
        }
        else if(operation=="^")
        {
            result=Math.pow(fnum, snum);
            res=String.format("%.2f",result);
            jdisplay.setText(res);
        }
        else if(operation=="p")
        {
            result=permutation(fnum,snum);
            res=String.format("%.2f",result);
            jdisplay.setText(res);
        }
        else if(operation=="c")
        {
            result=combination(fnum,snum);
            res=String.format("%.2f",result);
            jdisplay.setText(res);
        }
    }//GEN-LAST:event_jequalsActionPerformed

